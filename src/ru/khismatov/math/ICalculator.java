package ru.khismatov.math;

/**
 * Интерфейс калькулятора, определяющий операции для работы с числами.
 * Предоставляет метод для суммирования произвольного количества числовых значений.
 * Поддерживает работу с различными типами чисел, включая целые числа, дроби и числа с плавающей точкой.
 * 
 * Особенности:
 * - Поддерживает суммирование произвольного количества чисел
 * - Работает с любыми типами, реализующими интерфейс Number
 * - Возвращает результат в виде числа с плавающей точкой
 */
public interface ICalculator {
  /**
   * Суммирует произвольное количество числовых значений.
   * Все числа преобразуются в тип double перед суммированием.
   *
   * @param numbers набор чисел для суммирования
   * @return сумма всех чисел в виде числа с плавающей точкой
   */
  double sum(Number... numbers);
}
